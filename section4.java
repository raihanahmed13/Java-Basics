//Arithmetic Operator

class section4
{
	public static void main(String args[])
	{
		int a=5, b=7;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(++a);
		System.out.println(a++);
		System.out.println(--a);
		System.out.println(a--);
	}
}